    <p>No liquidificador, bata leite condensado, creme de leite e suco de maracujá até formar um creme firme.

Em um refratário, faça uma camada de biscoito maisena molhado no leite.

Coloque uma camada do creme de maracujá.

Repita as camadas até acabar os ingredientes.

Leve à geladeira por cerca de 3 horas.

Cobertura

Misture a polpa do maracujá com o açúcar.

Coloque por cima do pavê antes de servir.</p>

                    
